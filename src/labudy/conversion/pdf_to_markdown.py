def convert_pdf_to_markdown(pdf_path):
    """
    PDFファイルをMarkdown形式に変換するダミー関数

    Args:
        pdf_path (str): PDFファイルのパス

    Returns:
        str: Markdown形式のテキスト (ダミー)
    """
    print(f"PDFファイル '{pdf_path}' を Markdown に変換中... (ダミー)")
    dummy_markdown = f"""
# ダミーMarkdown変換結果

これはPDF '{pdf_path}' から変換されたダミーのMarkdownテキストです。

- 項目1
- 項目2
"""
    return dummy_markdown